/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
   u32 ulReturn;
  ulReturn = taskENTER_CRITICAL_FROM_ISR();

  if(__HAL_UART_GET_IT_SOURCE(&huart1, UART_IT_IDLE) != 0)
	{
		if(DmaToQueue(&huart1, &que_8bit) > 0){
			vTaskNotifyGiveFromISR( TaskUart8to9Handle, NULL );
		}
		__HAL_UART_CLEAR_IDLEFLAG(&huart1);
	}
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
  taskEXIT_CRITICAL_FROM_ISR( ulReturn );
  /* USER CODE END USART1_IRQn 1 */
}

/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
  u32 ulReturn;
  ulReturn = taskENTER_CRITICAL_FROM_ISR();

  if(__HAL_UART_GET_IT_SOURCE(&huart2, UART_IT_IDLE) != 0)
		{
      if(DmaToQueue(&huart2, &que_9bit) > 0){
        vTaskNotifyGiveFromISR( TaskUart9to8Handle, NULL );
      }
      __HAL_UART_CLEAR_IDLEFLAG(&huart2);
		}
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
taskEXIT_CRITICAL_FROM_ISR( ulReturn );
  /* USER CODE END USART2_IRQn 1 */
}

